// ==================== DEFAULT LANGUAGE ====================

const DEFAULT_LANG = 'fr'

// ==================== UTILITY FUNCTIONS ====================

/**
 * Get a translated string for a given key and language.
 * Falls back to French, then to the key itself.
 */
export function getTranslation(key: string, lang: string): string {
  const dict = dictionaries[lang]
  if (dict && dict[key]) {
    return dict[key]
  }
  // Fallback to French
  const frDict = dictionaries[DEFAULT_LANG]
  if (frDict && frDict[key]) {
    return frDict[key]
  }
  // Fallback to key itself
  return key
}

/**
 * Convert a URL short code (e.g. 'pi') to a language code (e.g. 'pidgin').
 * Returns the default language code if not found.
 */
export function getLangFromUrl(urlCode: string): string {
  const lang = LANGUAGES.find((l) => l.urlCode === urlCode)
  return lang ? lang.code : DEFAULT_LANG
}

/**
 * Convert a language code (e.g. 'pidgin') to a URL short code (e.g. 'pi').
 * Returns the default URL code if not found.
 */
export function getUrlCode(langCode: string): string {
  const lang = LANGUAGES.find((l) => l.code === langCode)
  return lang ? lang.urlCode : LANGUAGES[0].urlCode
}

/**
 * Build a URL language path like '/en', '/pi', etc.
 * Returns '/' for the default language (French).
 */
export function buildLangPath(langCode: string): string {
  if (langCode === DEFAULT_LANG) return '/'
  return `/${getUrlCode(langCode)}`
}

// ==================== ADDITIONAL UTILITY FUNCTIONS ====================

/**
 * Returns all translation keys from the French dictionary.
 * French is the reference dictionary with the most complete set of keys.
 */
export function getAllTranslationKeys(): string[] {
  return Object.keys(dictionaries['fr'] || {})
}

/**
 * Returns the French text for a given translation key.
 * Used as the source text for AI translation.
 */
export function getFrenchText(key: string): string {
  return dictionaries['fr']?.[key] || key
}

// ==================== REACT HOOK ====================

/**
 * Hook that provides translation function and language state from the Zustand store.
 *
 * Usage:
 *   const { t, lang, setLang, isTranslating } = useT()
 *   <h1>{t('hero.welcome')}</h1>
 *
 * This hook must be used inside a client component.
 * It reads `currentLanguage` and `setCurrentLanguage` from the Zustand app store.
 * When language changes, it automatically triggers batch translation of missing keys.
 */
export function useT() {
  // eslint-disable-next-line @typescript-eslint/no-require-imports
  const { useAppStore } = require('@/components/agryva/store') as { useAppStore: any }
  const currentLanguage = useAppStore((s: any) => (s as any).currentLanguage) ?? DEFAULT_LANG
  const dynamicTranslations = useAppStore((s: any) => (s as any).dynamicTranslations) ?? {}
  const translationVersion = useAppStore((s: any) => (s as any).translationVersion) ?? 0
  const setDynamicTranslations = useAppStore((s: any) => (s as any).setDynamicTranslations)
  const incrementTranslationVersion = useAppStore((s: any) => (s as any).incrementTranslationVersion)
  const setCurrentLanguage = useAppStore((s: any) => (s as any).setCurrentLanguage)

  const lang = currentLanguage || DEFAULT_LANG

  // Use translationVersion to force re-render when dynamic translations update
  void translationVersion

  const t = (key: string): string => {
    // 1. Check dynamic translations first
    const dynamicKey = `${lang}:${key}`
    if (dynamicTranslations[dynamicKey]) {
      return dynamicTranslations[dynamicKey]
    }
    // 2. Check static dictionary
    return getTranslation(key, lang)
  }

  const setLang = (code: string): void => {
    if (setCurrentLanguage) {
      (setCurrentLanguage as (code: string) => void)(code)
    }
    // No URL redirect — the SPA is a single route.
    // Zustand state change triggers reactive re-render of all t() calls.
  }

  // Auto-translate missing keys when language changes
  // eslint-disable-next-line @typescript-eslint/no-require-imports
  const { useEffect } = require('react') as { useEffect: any }

  useEffect(() => {
    if (typeof window === 'undefined') return
    if (lang === 'fr') return // French is complete

    let cancelled = false
    let timeoutId: ReturnType<typeof setTimeout>

    const translateMissing = async () => {
      const allKeys = getAllTranslationKeys()
      const dict = (dictionaries as any)[lang] || {}

      // Find keys missing from both static dict and dynamic cache
      const missing = allKeys.filter((key: string) => {
        if (dict[key]) return false // Already in static dict
        const dynamicKey = `${lang}:${key}`
        if (dynamicTranslations[dynamicKey]) return false // Already translated
        return true
      })

      if (missing.length === 0 || cancelled) return

      try {
        const pairs = missing.map((key: string) => ({
          key,
          text: getFrenchText(key),
        }))

        // Split into chunks of 20 to avoid too-large prompts
        const chunkSize = 20
        const allTranslations: Record<string, string> = { ...dynamicTranslations }

        for (let i = 0; i < pairs.length; i += chunkSize) {
          if (cancelled) return
          const chunk = pairs.slice(i, i + chunkSize)
          const res = await fetch('/api/agryva/ai/translate-batch', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ targetLanguage: lang, pairs: chunk }),
          })
          const json = await res.json()
          if (json.success && json.translations) {
            for (const [key, value] of Object.entries(json.translations)) {
              allTranslations[`${lang}:${key}`] = value as string
            }
            // Update progressively for better UX
            if (setDynamicTranslations) {
              setDynamicTranslations({ ...allTranslations })
            }
          }
        }

        if (!cancelled && incrementTranslationVersion) {
          incrementTranslationVersion()
        }
      } catch (err) {
        console.warn('[useT] Batch translation failed:', err)
      }
    }

    // Debounce to avoid rapid re-translations
    timeoutId = setTimeout(translateMissing, 300)

    return () => {
      cancelled = true
      clearTimeout(timeoutId)
    }
  }, [lang, dynamicTranslations])

  const isTranslating = false // Could be enhanced with a proper state
  return { t, lang, setLang, isTranslating }
}
